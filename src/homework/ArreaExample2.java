package homework;

public class ArreaExample2 {
/*
  * * * *
  * * *
  * *
  *
 */

    public static void main(String[] args) {
        char a = 42;
        for (int i = 4; i <= 4; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print(a + " ");
            }
            System.out.println(" ");

            for (int k = 1; k <= 3; k++) {
                System.out.print(a + " ");

            }


            System.out.println(" ");
        }
        System.out.println(a + " * ");
        System.out.println("*");
    }


}
